namespace Calculadora
{

    public class SmartData
    {
        public double desvio;
        public double height;
        public Club club;
        public Options options;

        public SmartData(double desvio, double height, Club club, Options options)
        {
            this.desvio = desvio;
            this.height = height;
            this.club = club;
            this.options = options;
        }

    }

}
